class QuartzBlock extends SolidBlock{
	public function __construct($meta = 0){
		parent::__construct(QUARTZ_BLOCK, $meta, "Quartz Block");
		$names = array(
			0 => "Quartz Block",
			1 => "Chiseled Quartz Block",
			2 => "Quartz Pillar",
			3 => "Quartz Pillar",
		);
		$this->name = $names[$this->meta & 0x03];
	}
	
}